You are hired by a company to determine if a specific type of beam is suitable for an upcoming
project. They have data on the modulus of rupture (breaking strength) of 20 of these wooden 
beams. (modulus data)

a) At the 95% confidence level, do we have evidence that the average modulus of rupture 
is greater than 5975 lbs/in^2?

b) At the 95% confidence level, do we have evidence that the average modulus of rupture 
is within the desired bounds 5985-6015 lbs/in^2?

c) At the 95% confidence level, how many beams would need to be broken such 
that we could determine the average modulus of rupture with a margin of error 
of no more than 10 lbs/in^2?